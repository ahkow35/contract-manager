def test_image_preservation_in_runs(tmp_path):
    """
    Test that images embedded in runs are not removed during text replacement.
    """
    # 1. Create a template DOCX with an image
    doc = Document()
    p = doc.add_paragraph()
    run = p.add_run()
    run.add_text("Start ")
    
    # Add a picture to the run
    image_stream = BytesIO(MINIMAL_JPEG)
    image_stream.name = "test.jpg"
    run.add_picture(image_stream, width=Inches(1.0))
    
    run.add_text(" End")
    
    # Save template
    template_path = tmp_path / "image_template.docx"
    doc.save(str(template_path))
    
    # 2. Run generate_docx (no replacements needed to trigger the bug if it iterates all runs)
    # The bug is likely that iterating and setting run.text = run.text wipes the image
    output_bytes = generate_docx(str(template_path), {})
    
    # 3. Verify output
    output_doc = Document(BytesIO(output_bytes))
    
    # Check if image is present
    # In python-docx, inline shapes are stored in the document or run
    # easiest way is to check the XML or relationships, but let's check inline_shapes on the run if available
    # or check the XML length/content
    
    # Let's inspect the first paragraph
    p_out = output_doc.paragraphs[0]
    run_out = p_out.runs[0]
    
    # Check for drawing elements in the XML
    # The 'w:drawing' tag indicates an image/shape
    assert "w:drawing" in run_out.element.xml, "Image (drawing) element was removed from the run!"
